
public class OperadoresAritmeticos {

	public static void main(String[] args) {
		int minhaIdade = 40;
		int suaIdade = 25;

		// Adição
		System.out.println("Adição:");
		int totalIdades = minhaIdade + suaIdade + 100;
		System.out.println("Soma das idades: " + totalIdades);
		System.out.println();
		// Subtração
		System.out.println("Subtração:");
		int diferencasIdades = minhaIdade - suaIdade;
		System.out.println("Diferença das idades: " + diferencasIdades);
		System.out.println();
		// Subtração
		System.out.println("Multiplicação:");
		int dobroDaIdade = minhaIdade * 2;
		System.out.println("Dobro da idade: " + dobroDaIdade);
		System.out.println();
		// Divisao
		System.out.println("Divisão:");
		int metadeDaIdade = suaIdade / 2;
		System.out.println("Metade da idade: " + metadeDaIdade);
		System.out.println();
		// Resto da Divisao
		System.out.println("Resto da Divisão:");
		int RestoDivisao = 7 % 2;
		System.out.println("Metade da idade: " + RestoDivisao);
		System.out.println();

	}

}
